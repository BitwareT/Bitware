        <h1>Política de Privacidad de Bitware</h1>
        <p><strong>Última actualización:</strong> 9 de octubre de 2025</p>
        <p>En Bitware, respetamos su privacidad y nos comprometemos a proteger sus datos personales de acuerdo con la Ley N° 19.628 sobre Protección de la Vida Privada. Esta política detalla cómo recopilamos, usamos y protegemos su información.</p>

        <h2>Aviso Importante: Proyecto Académico (Proyecto de Título)</h2>
        <p>Se deja constancia de que <strong>Bitware</strong> es un sitio web desarrollado como parte de un <strong>proyecto de título</strong> con fines exclusivamente académicos y de demostración.</p>
        <p>Este no es un comercio electrónico funcional. <strong>Toda la información personal solicitada (como nombre, dirección o teléfono) se utiliza únicamente para simular el funcionamiento del sitio y no será utilizada con fines comerciales reales.</strong> Aunque el sistema almacena datos para la demostración (por ejemplo, para crear una cuenta de usuario o simular un pedido), se recomienda <strong>no utilizar datos personales reales</strong>.</p>
        <hr>
        <h2>1. Información que Recopilamos</h2>
        <p>Recopilamos información para procesar sus pedidos y ofrecerle una experiencia de usuario personalizada. Los datos recopilados incluyen:</p>
        <ul>
            <li><strong>Información de Identificación Personal:</strong> Nombre, correo electrónico, dirección de envío y número de teléfono.</li>
            <li><strong>Datos de la Cuenta de Usuario:</strong> Contraseña (almacenada de forma segura mediante cifrado hash) y preferencias de perfil.</li>
            <li><strong>Información de Transacciones:</strong> Detalles de sus compras, historial de pedidos y solicitudes de servicio.</li>
            <li><strong>Comunicaciones:</strong> Mensajes y consultas enviadas a través de nuestro formulario de contacto, chatbot o correo electrónico.</li>
        </ul>

        <h2>2. Cómo Usamos su Información</h2>
        <p>Su información personal se utiliza para los siguientes propósitos:</p>
        <ul>
            <li><strong>Procesamiento de Pedidos:</strong> Para gestionar pagos, realizar envíos y procesar devoluciones o garantías.</li>
            <li><strong>Gestión de su Cuenta:</strong> Para permitirle acceder a su historial, editar su perfil y administrar sus datos.</li>
            <li><strong>Comunicación y Soporte al Cliente:</strong> Para enviarle confirmaciones de pedido, notificaciones de envío y responder a sus consultas.</li>
            <li><strong>Mejora de nuestros Servicios:</strong> Para analizar datos de uso de forma anónima y mejorar la funcionalidad y oferta de nuestro sitio web.</li>
        </ul>

        <h2>3. Con Quién Compartimos su Información</h2>
        <p>No vendemos, alquilamos ni transferimos su información personal a terceros no relacionados con la operación de nuestro servicio. Solo compartimos sus datos con:</p>
        <ul>
            <li><strong>Proveedores de Servicios de Pago:</strong> Para procesar sus transacciones de forma segura.</li>
            <li><strong>Empresas de Logística y Envío:</strong> Para entregarle sus productos en la dirección correcta.</li>
            <li><strong>Requerimiento Legal:</strong> Si es requerido por una autoridad judicial o gubernamental competente en el marco de la ley.</li>
        </ul>

        <h2>4. Seguridad de sus Datos</h2>
        <p>Tomamos medidas de seguridad técnicas y organizativas para proteger sus datos contra pérdida, acceso no autorizado o alteración. Esto incluye el uso de cifrado para las contraseñas (`password_hash`) y sentencias preparadas para proteger nuestra base de datos.</p>

        <h2>5. Sus Derechos (Derechos ARCO)</h2>
        <p>Usted tiene derecho a <strong>Acceder, Rectificar, Cancelar y Oponerse</strong> al tratamiento de sus datos personales.</p>
        <ul>
            <li>Puede <strong>acceder y rectificar</strong> la mayoría de sus datos directamente desde su panel de "Editar Perfil" una vez iniciada la sesión.</li>
            <li>Para ejercer su derecho de <strong>cancelación u oposición</strong>, o para cualquier otra consulta sobre sus datos, puede contactarnos a través de nuestro formulario de contacto.</li>
        </ul>

        <h2>6. Uso de Cookies</h2>
        <p>Utilizamos cookies que son esenciales para el funcionamiento del sitio web, como por ejemplo, para mantener su sesión iniciada o recordar los productos en su carrito de compras. No utilizamos cookies de seguimiento con fines publicitarios sin su consentimiento explícito.</p>

        <h2>7. Contacto</h2>
        <p>Si tiene alguna pregunta sobre esta política de privacidad o sobre el tratamiento de sus datos, no dude en contactarnos a través de la sección "Contacto" de nuestro sitio web.</p>
